// 1. Define a piece of state (like description)
// 2. We use that state on the element that we want to control, we force the element
// to always take the value of state variable (value={description})
// 3. We update that state variable. We do with onChange handler, where we set description
// to the current value of that input field
// => React incharge of the state and of the entire element
